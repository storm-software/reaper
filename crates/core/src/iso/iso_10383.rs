//! This module contains the ISO 10383 market identifier codes.
//! These are used to identify securities trading exchanges and regulated markets.
//! Exchanges usually have a calendar associated with them, which is used to determine
//! whether a date is a business day or not.

/// ISO 10383 market identifier codes enum.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[allow(non_camel_case_types)]
pub struct ISO_10383 {
  /// The ISO 10383 operating MIC.
  pub operating_mic: &'static str,

  /// The ISO 3166-1 alpha-2 country code.
  pub country_code: &'static str,

  /// The ISO 10383 operating/segment flag.
  pub oprt_sgmt: OperatingOrSegment,

  /// Status flag.
  pub status: MICStatus,

  /// City.
  pub city: &'static str,

  /// The ISO 10383 market name.
  pub description: &'static str,
}

/// Operating/segment flag.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum OperatingOrSegment {
  /// Operating MIC.
  Operating,

  /// Segment MIC.
  Segment,
}

/// MIC status flag.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[allow(non_camel_case_types)]
pub enum MICStatus {
  /// Active.
  Active,

  /// Updated.
  Updated,

  /// Expired.
  Expired,
}

/// Macro to read all ISO 10383 codes from CSV file and generate all constants.
macro_rules! iso_10383 {
    // [ mic, operating_mic, iso_3166, oprt_sgmt, status, city, description ]
    ($(
        $mic:ident:
        $operating_mic:literal,
        $iso_3166:literal,
        $oprt_sgmt:ident,
        $status:ident,
        $city:literal,
        $description:literal,
    )*) => {
        $(
            /// $description
            pub const $mic: ISO_10383 = ISO_10383 {
                operating_mic: $operating_mic,
                country_code: $iso_3166,
                oprt_sgmt: OperatingOrSegment::$oprt_sgmt,
                status: MICStatus::$status,
                city: $city,
                description: $description,
            };
        )*
    };
}

